export function slugifyProjectName(name) {
  return name
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

export const projectsWithSlug = projectsData.map((project) => ({
  ...project,
  slug: slugifyProjectName(project.name),
}));

export function getProjectTranslation(project, language = "pt") {
  return {
    ...project,
    category: project.category?.[language] || project.category?.pt || "",
    highlight: project.highlight?.[language] || project.highlight?.pt || "",
    description: project.description?.[language] || project.description?.pt || "",
  };
}
